package OOP_Review.Inheritance;

public class Operations {
    public int a;
    public int b;
    public void add (int a, int b){
        System.out.println(a+b);
    }
   private void add(int a, int b, int c){
        System.out.println(a+b+c);
    }
    public void multiply (int a, int b){
        System.out.println(a*b);
    }
}
