package creating_classes;

public class Shapes {
    char Symbol;
    int Width;
    int height;

    public Shapes() {
        this.Symbol = '*';
        this.Width = 10;
        this.height = 10;
    }
        public Shapes(char symbol, int width, int height) {
        Symbol = symbol;
        Width = width;
        this.height = height;
    }
    

}
